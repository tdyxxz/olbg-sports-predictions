CREATE TABLE IF NOT EXISTS snooker_matches (
    event_id TEXT PRIMARY KEY,
    event_date TEXT NOT NULL,
    season TEXT,
    tournament TEXT NOT NULL,
    round TEXT,
    best_of INTEGER,
    player_a TEXT NOT NULL,
    player_b TEXT NOT NULL,
    rank_a INTEGER,
    rank_b INTEGER,
    last5_wins_a INTEGER,
    last5_wins_b INTEGER,
    last5_frame_diff_a INTEGER,
    last5_frame_diff_b INTEGER,
    in_tournament_wins_a INTEGER,
    in_tournament_wins_b INTEGER,
    in_tournament_frame_diff_a INTEGER,
    in_tournament_frame_diff_b INTEGER,
    rest_days_a REAL,
    rest_days_b REAL,
    prev_decider_a INTEGER,
    prev_decider_b INTEGER,
    h2h_3y_wins_a INTEGER,
    h2h_3y_wins_b INTEGER,
    model_prob_a REAL NOT NULL,
    price_taken_a REAL,
    price_taken_b REAL,
    close_odds_a REAL NOT NULL,
    close_odds_b REAL NOT NULL,
    winner TEXT NOT NULL,
    score_a INTEGER,
    score_b INTEGER,
    notes TEXT
);
